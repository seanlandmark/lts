USE [Invoice];
GO

IF OBJECT_ID(N'dbo.PCT_DUMP', N'U') IS NOT NULL
BEGIN
    DROP TABLE dbo.PCT_DUMP;
END;
GO

CREATE TABLE dbo.PCT_DUMP
(
    ID INT NULL,
    Date_of_Service DATE NULL,
    Time_of_Service TIME(0) NULL,
    Patient_Name NVARCHAR(255) NULL,
    dob_value NVARCHAR(50) NULL,
    CSN_MRN NVARCHAR(100) NULL,
    FORM_ID INT NULL,
    LEVEL_OF_SERVICE NVARCHAR(100) NULL,
    PICKUP_LOCATION NVARCHAR(500) NULL,
    DROPOFF_LOCATION NVARCHAR(500) NULL,
    NOTES NVARCHAR(MAX) NULL,
    WAIT_TIME NVARCHAR(50) NULL,
    ORDER_TOTAL_AMOUNT DECIMAL(12,2) NULL,
    DISTANCE DECIMAL(12,4) NULL,
    DURATION NVARCHAR(20) NULL
);
GO

BULK INSERT dbo.PCT_DUMP
FROM 'D:\LHS\pct_bookings_extract.csv'
WITH
(
    FORMAT = 'CSV',
    FIRSTROW = 2,
    FIELDQUOTE = '"',
    FIELDTERMINATOR = ',',
    ROWTERMINATOR = '0x0a',
    CODEPAGE = '65001',
    TABLOCK,
    KEEPNULLS
);
GO

SELECT COUNT(*) AS imported_rows FROM dbo.PCT_DUMP;

SELECT TOP (5)
    ID,
    Date_of_Service,
    Time_of_Service,
    Patient_Name,
    FORM_ID,
    LEVEL_OF_SERVICE,
    ORDER_TOTAL_AMOUNT,
    DISTANCE,
    DURATION
FROM dbo.PCT_DUMP
ORDER BY ID;
GO
